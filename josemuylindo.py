# Dimensiones de la matriz
m = int(input("No. de filas de la Matriz: "))
n = int(input("No. de columnas de la Matriz: "))

# Creación de la matriz

M = []

for i in range(m):
  M.append([])
  for j in range(n):
    M[i].append(random.randint(1,9))

